#!/usr/bin/python3

"""Establishes a base model class."""
import json
import csv
import turtle


class Base:
    """The Base model.

    This serves as the project 0x0C basic class for all other classes.

    Private Class Attributes:
        __nb_object (int): Instances of Bases instantiated.
    """

    __nb_objects = 0

    def __init__(self, id=None):
        """Start a fresh Base.

        Args:
            id (int): Describes the new Base name.
        """
        if id is not None:
            self.id = id
        else:
            Base.__nb_objects += 1
            self.id = Base.__nb_objects

    @staticmethod
    def to_json_string(list_dictionaries):
        """Return a list of dictionaries serialized in JSON.

        Args:
            list_dictionaries (list): dictionaries listed.
        """
        if list_dictionaries is None or list_dictionaries == []:
            return "[]"
        return json.dumps(list_dictionaries)

    @classmethod
    def save_to_file(cls, list_objs):
        """Create file with JSON serialization of a collection of objects.

        Args:
            list_objs (list): Instances of inherited Base that are listed.
        """
        filename = cls.__name__ + ".json"
        with open(filename, "w") as jsonfile:
            if list_objs is None:
                jsonfile.write("[]")
            else:
                list_dicts = [o.to_dictionary() for o in list_objs]
                jsonfile.write(Base.to_json_string(list_dicts))

    @staticmethod
    def from_json_string(json_string):
        """Get a JSON string deserialization back.

        Args:
            json_string (str): list of dictionaries represented as JSON str.
        Returns:
            If json_string is empty or None a list will be created.
            Otherwise - the Python list that json_string represents.
        """
        if json_string is None or json_string == "[]":
            return []
        return json.loads(json_string)

    @classmethod
    def create(cls, **dictionary):
        """Bring back class that was created using dictionary of attributes.

        Args:
            **dictionary (dict): Attribute initialization with key-value pairs.
        """
        if dictionary and dictionary != {}:
            if cls.__name__ == "Rectangle":
                new = cls(1, 1)
            else:
                new = cls(1)
            new.update(**dictionary)
            return new

    @classmethod
    def load_from_file(cls):
        """Bring back a list of classes created from a file of JSON strings.

        Reads from `<cls.__name__>.json`.

        Returns:
            An empty list appears if the file does not exist.
            Otherwise - The classes that have been instantiated.
        """
        filename = str(cls.__name__) + ".json"
        try:
            with open(filename, "r") as jsonfile:
                list_dicts = Base.from_json_string(jsonfile.read())
                return [cls.create(**d) for d in list_dicts]
        except IOError:
            return []

    @classmethod
    def save_to_file_csv(cls, list_objs):
        """Create a file and serialize list of items in CSV format.

        Args:
            list_objs (list): A listing of inherited Base instances.
        """
        filename = cls.__name__ + ".csv"
        with open(filename, "w", newline="") as csvfile:
            if list_objs is None or list_objs == []:
                csvfile.write("[]")
            else:
                if cls.__name__ == "Rectangle":
                    fieldnames = ["id", "width", "height", "x", "y"]
                else:
                    fieldnames = ["id", "size", "x", "y"]
                writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
                for obj in list_objs:
                    writer.writerow(obj.to_dictionary())

    @classmethod
    def load_from_file_csv(cls):
        """Give back a list of classes that were created using CSV file.

        Reads from `<cls.__name__>.csv`.

        Returns:
            An empty list appears if the file is nonexistent.
            Otherwise - A listing of inherited Base instances.
        """
        filename = cls.__name__ + ".csv"
        try:
            with open(filename, "r", newline="") as csvfile:
                if cls.__name__ == "Rectangle":
                    fieldnames = ["id", "width", "height", "x", "y"]
                else:
                    fieldnames = ["id", "size", "x", "y"]
                list_dicts = csv.DictReader(csvfile, fieldnames=fieldnames)
                list_dicts = [dict([k, int(v)] for k, v in d.items())
                              for d in list_dicts]
                return [cls.create(**d) for d in list_dicts]
        except IOError:
            return []

    @staticmethod
    def draw(list_rectangles, list_squares):
        """Use the turtle module to draw rectangles and squares.

        Args:
            list_rectangles (list): A list of things to draw in rectangles.
            list_squares (list): A list of things to draw in Square.
        """
        turt = turtle.Turtle()
        turt.screen.bgcolor("#b7312c")
        turt.pensize(3)
        turt.shape("turtle")

        turt.color("#ffffff")
        for rect in list_rectangles:
            turt.showturtle()
            turt.up()
            turt.goto(rect.x, rect.y)
            turt.down()
            for i in range(2):
                turt.forward(rect.width)
                turt.left(90)
                turt.forward(rect.height)
                turt.left(90)
            turt.hideturtle()

        turt.color("#b5e3d8")
        for sq in list_squares:
            turt.showturtle()
            turt.up()
            turt.goto(sq.x, sq.y)
            turt.down()
            for i in range(2):
                turt.forward(sq.width)
                turt.left(90)
                turt.forward(sq.height)
                turt.left(90)
            turt.hideturtle()

        turtle.exitonclick()
